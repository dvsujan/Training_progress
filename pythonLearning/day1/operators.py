x = 10 ; 
y = 20 ; 

# basic operators
print(x + y) # addition
print(x - y) # subtraction
print(x * y) # multiplication
print(x / y) # division
print(x // y) # floor division
print(x % y) # modulus
print(x ** y) # power

#comparision operators
print (x == y) # equal to
print (x != y) # not equal to
print (x > y) # greater than
print (x < y) # less than
print (x >= y) # greater than or equal to
print (x <= y) # less than or equal to
